<h1 class="center-align">O que é o Sistema AGP</h1>
    <p>Ganhar eleição para vereador nem sempre é fácil. Afinal, além da enorme quantidade de
candidatos, a sua coligação precisa atingir o coeficiente eleitoral da cidade. Nesse contexto, é
essencial ter um bom planejamento de campanha a fim de conseguir o maior número de votos
válidos possível.</p>
A boa notícia é que existem algumas estratégias que, se seguidas à risca, vão te ajudar a ter
sucesso no pleito. Entretanto, é importante destacar que é fundamental começar a trabalhar
desde já!</p>

<h2>1. Represente uma causa</h2>
<p>O candidato que não tem um público-alvo bem definido dificilmente terá sucesso, já que as
estratégias não serão direcionadas a ninguém em específico. Nesse sentido, é essencial se
posicionar sobre que tipo de político você é e quem representa.</p>
<p>Se você vai tentar a reeleição, o recomendado é continuar com o discurso e alimentar a sua
relação com os eleitores. Já se você for um candidato de primeira viagem, será preciso
identificar, em seu convívio social, quem é o seu público-alvo.</p>
<p>Um jovem de 18 anos pode representar os estudantes, por exemplo. Já um banqueiro
consegue construir o seu discurso para os empresários e assim por diante.</p>


<h2>2. Alie o seu discurso e sua imagem com o perfil do público-alvo</h2>
<p>Definido quem é o seu público-alvo, é importante que todas as suas estratégias sejam voltadas
a ele. Para isso, é essencial identificar quais são os principais anseios, dificuldades e queixas
daquelas pessoas. A partir daí, um discurso deve ser criado.</p>
<p>No entanto, é preciso salientar que o discurso que você vai adotar deve estar de acordo com a
sua história e trajetória política. Estamos vivendo um período de profunda transformação e,
diferente do passado, as pessoas não têm memória tão curta mais.</p>
<p>Além do tom da mensagem e das propostas que serão apresentadas, também é fundamental
que outros aspectos estejam de acordo com o perfil do eleitor, como:
vestimenta do político (mais séria? informal?);
cores usadas nos materiais de divulgação;
principais temas abordados durante a campanha (saúde, educação, direitos humanos etc.);
linguajar utilizado em eventos, entre outros.</p>   

<h2>3. Recrute pessoas para trabalhar com você</h2>
<p>Você já deve imaginar que é impossível ganhar uma eleição sem uma equipe bem estruturada,
certo? Sendo assim, é importante recrutar, antecipadamente, as pessoas que te ajudarão na
campanha.</p>
<p>Nesse momento, tente mesclar profissionais de diferentes habilidades e competências. Se
você está tentando a reeleição, é importante que os seus assessores já estejam trabalhando
em prol das eleições.</p>
<p>É claro, porém, que ninguém vai pedir voto antecipado. Mas atender bem os eleitores no
gabinete, mandar e-mails para eles constantemente e construir uma imagem forte do
candidato na internet são exemplos de práticas que devem ser adotadas ainda no primeiro dia
do mandato.</p>  

<h2>4. Construa uma rede de apoiadores</h2>
<p>Construir uma rede de apoiadores é essencial para qualquer político. Para isso, é importante te
um banco de dados com os contatos dos seus potenciais eleitores.
<p>Sempre que possível, tente segmentar a sua lista por idade, bairro, sexo, entre outros. Desse
modo, você terá a certeza que vai atingir o público certo no momento exato. Por exemplo,
vamos supor que você é um vereador que quer se reeleger.</p>
<p>Em determinado momento do mandato, você apresentou um projeto para a construção de um
parque no bairro X. Com uma lista segmentada em mãos, você poderá mandar mensagens
divulgando a sua ação para os eleitores daquela região. Afinal, quem mora do outro lado da
cidade não está muito interessado no assunto, concorda?</p>
<p>Atualmente, existem vários softwares que criam bancos de dados segmentados
automaticamente para você. Não se esqueça, porém, que, sempre que possível, é importante
marcar reuniões e encontros com os apoiadores pessoalmente.</p> 

<h2>5. Aproveite o poder das mídias sociais</h2>
<p> As redes sociais são excelentes ferramentas para o político. Entretanto, para alcançar
resultados satisfatórios, o importante é ter estratégia. O primeiro passo, portanto, é contratar
um profissional capacitado para cuidar da sua imagem na internet.</p>
<p> Feito isso, será preciso estabelecer quais canais de comunicação serão utilizados. Para isso, é
essencial descobrir em quais mídias o seu público-alvo está (Facebook, Instagram, Snapchat,
Twitter, LinkedIn etc.).Definidas quais redes serão utilizadas, faça um cronograma de postagens organizado e
detalhado, com os tipos de conteúdo que serão postados e os seus respectivos horários.</p>
<p>Lembre-se de que, em todo o caso, o ideal é utilizar a internet para interagir com os seus
eleitores, além de promover suas ações e ideais. Vale ressaltar, ainda, que a linguagem
adotada, assim como o material gráfico, deve estar de acordo com o perfil do seu eleitor.</p>

<h2>6. Esteja preparado para os pedidos inusitados</h2>
<p>Quem tem um mandato sabe a quantidade de solicitações inusitadas que chegam diariamente
ao gabinete — desde pedido de emprego até carne para churrasco.</p>
<p>Nesse momento, é importante que toda a equipe esteja preparada para responder
educadamente demandas como essas. O ideal é que você construa um discurso firme, gentil e
solícito para negar os pedidos.</p>
<p>Já as solicitações sérias devem ser tratadas com o máximo de zelo possível. Tente sempre dar
um feedback para o eleitor informando-o como está o andamento de sua solicitação.</p>

<h2>7. Faça um cronograma de ações</h2>
<p>Para que suas estratégias deem certo, é preciso planejar-se. Portanto, é muito importante que
você faça um cronograma estabelecendo metas e estipulando prazos.</p>
<p>Defina, dentre outras coisas, quem e quantos apoiadores você vai visitar durante uma semana.
Além disso, tenha alguém responsável por identificar quais são os eventos da região que é
importante marcar presença. Lembre-se de que, para ganhar uma eleição, você deve ser visto
— tanto pessoalmente, quanto no mundo online.</p>
<p>Além de definir o cronograma, é fundamental realizar reuniões semanais com a sua equipe a
fim de fiscalizar como está o andamento do trabalho.</p>


